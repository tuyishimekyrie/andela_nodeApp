import mongoose from "mongoose";
import  genreSchema  from "./genresSchema";

const Movie = mongoose.model(
  "Movies",
  new mongoose.Schema({
    title: {
      type: String,
      required: true,
      trim: true,
      minlength: 5,
      maxlength: 255,
    },
    genre: {
      type: mongoose.Schema.Types.ObjectId, // Use ObjectId type for referencing another schema
      ref: "Genre",
      required: true,
    },
    numberInStock: {
      type: Number,
      required: true,
      min: 0,
      max: 255,
    },
    dailyRentalRate: {
      type: Number,
      required: true,
      min: 0,
      max: 255,
    },
  })
);

export default Movie;
